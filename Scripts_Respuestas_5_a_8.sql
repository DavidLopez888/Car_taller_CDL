SELECT *
FROM   T02_TERCEROS T02
       INNER JOIN T04_FACTURAS T04 ON T04.F04_ROWID_TERCERO_CLIENTE = T02.F02_ROWID_TERCERO
WHERE  T04.F04_VLR_NETO >= 100000
AND    T04.F04_FECHA_DOCTO >= ADD_MONTHS(SYSDATE, -2);
--R/ 6
SELECT F_ID_ITEM
FROM   (SELECT T01.F01_ROWID_ITEM,
               T01.F01_ID_ITEM F_ID_ITEM,
               T04.F04_FECHA_DOCTO F_FECHA_DOCTO,
               SUM(T041.F041_CANT_ITEM) F_CANT_VEND
		    FROM   T041_MOVTO_FACTURA T041
               INNER JOIN T01_ITEMS T01 ON T01.F01_ROWID_ITEM = T041.F041_ROWID_ITEM
               INNER JOIN T04_FACTURAS T04 ON T04.F04_ROWID_FACTURA = T041.F041_ROWID_FACTURA
        GROUP  BY T01.F01_ROWID_ITEM,T01.F01_ID_ITEM,T04.F04_FECHA_DOCTO)        
WHERE  F_FECHA_DOCTO >= ADD_MONTHS(SYSDATE, -1)
AND    ROWNUM <= 100
ORDER  BY F_CANT_VEND DESC;
--R/ 7
SELECT F_ID_ITEM,
       F_NOM_TIENDA
FROM   (SELECT T01.F01_ROWID_ITEM,
               T01.F01_ID_ITEM F_ID_ITEM,
               T08.F08_NOMBRE_TIENDA F_NOM_TIENDA,
               T04.F04_FECHA_DOCTO F_FECHA_DOCTO,
               SUM(T041.F041_CANT_ITEM) F_CANT_VEND
		    FROM   T041_MOVTO_FACTURA T041
               INNER JOIN T01_ITEMS T01 ON T01.F01_ROWID_ITEM = T041.F041_ROWID_ITEM
               INNER JOIN T04_FACTURAS T04 ON T04.F04_ROWID_FACTURA = T041.F041_ROWID_FACTURA
               INNER JOIN T08_TIENDA T08 ON T08.F08_ROWID_TIENDA = T04.F04_ROWID_TIENDA
        GROUP  BY T01.F01_ROWID_ITEM,T01.F01_ID_ITEM,T08.F08_NOMBRE_TIENDA,T04.F04_FECHA_DOCTO)        
WHERE  F_FECHA_DOCTO >= ADD_MONTHS(SYSDATE, -2)
AND    F_ID_ITEM => '100';
--R/ 8
SELECT F_RAZON_SOCIAL
FROM   (SELECT T02.F02_ROWID_TERCERO,
               T02.F02_RAZON_SOCIAL F_RAZON_SOCIAL,
               T04.F04_FECHA_DOCTO F_FECHA_DOCTO,
               SUM(T041.F041_CANT_ITEM) F_CANT_VEND
		    FROM   T041_MOVTO_FACTURA T041
               INNER JOIN T04_FACTURAS T04 ON T04.F04_ROWID_FACTURA = T041.F041_ROWID_FACTURA
               INNER JOIN T02_TERCEROS T02 ON T02.F02_ROWID_TERCERO = T04.F04_ROWID_TERCERO_CLIENTE
               INNER JOIN T05_MANTENIMIENTO T05 ON T05.F05_ROWID_MANTO = T041.F041_ROWID_MANTO               
        GROUP  BY T02.F02_ROWID_TERCERO,T02.F02_RAZON_SOCIAL,T04.F04_FECHA_DOCTO)        
WHERE  F_FECHA_DOCTO >= ADD_MONTHS(SYSDATE, -1)
AND    F_CANT_VEND > 1;
